# Prints all numbers
for i in [0, 1, 2, 3, 4]:
    print(i)
    
for i in range(5): # range between 0 to 4
    print(i)
    
# Prints all the names in the list
names = ["Daniel", "German", "Valeria"]
for i in names:
    print(i)
    
# Prints every character of the string
name = "Claudia"
for letter in name:
    print(letter)